package vn7.studio.com.integrator.math.calculator.cord;

public class Cord3D {
    private Number x, y, z;

    public Cord3D(Number x, Number y, Number z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public Number getX() {
        return x;
    }

    public void setX(Number x) {
        this.x = x;
    }

    public Number getY() {
        return y;
    }

    public void setY(Number y) {
        this.y = y;
    }

    public Number getZ() {
        return z;
    }

    public void setZ(Number z) {
        this.z = z;
    }
}
